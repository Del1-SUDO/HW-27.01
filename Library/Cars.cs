namespace Library;
public class Cars
{
    public string Model { get; set; }
    public string Type { get; set; }
    public int Year { get; set; }
    public double Price { get; set; }

    public Cars(string model, string type, int year, double price)
    {
        this.Model = model;
        this.Type = type;
        this.Year = year;
        this.Price = price;
        this.Price = price;
    }
}
